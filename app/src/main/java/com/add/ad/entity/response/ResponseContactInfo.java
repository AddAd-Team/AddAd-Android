package com.add.ad.entity.response;

public class ResponseContactInfo {
    private String title;
    private int creatorId;
    private String creatorProfileImage;
    private String creatorName;
    private int advertiserId;
    private String advertiserProfileImage;
    private String advertiserName;

    public String getTitle() {
        return title;
    }

    public int getCreatorId() {
        return creatorId;
    }

    public String getCreatorProfileImage() {
        return creatorProfileImage;
    }

    public String getCreatorName() {
        return creatorName;
    }

    public int getAdvertiserId() {
        return advertiserId;
    }

    public String getAdvertiserProfileImage() {
        return advertiserProfileImage;
    }

    public String getAdvertiserName() {
        return advertiserName;
    }
}